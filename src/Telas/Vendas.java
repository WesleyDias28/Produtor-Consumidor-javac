    public void escreveTabela(){
        try {
            File file = new File("C:\\Users\\Wesley\\Documents\\NetBeansProjects\\ProjetoAED3_1\\src\\Relatorios\\Vendas.csv");

            if (!file.exists()) {
                file.createNewFile();
            }

            try (FileWriter fw = new FileWriter(file, true); BufferedWriter bw = new BufferedWriter(fw)) {
                //for (int i = 0; i < jTableProdutos.getColumnCount(); i++) {
                    //bw.append(jTableProdutos.getModel().getColumnName(i) + ";");
                //}
                //bw.newLine();
                for (int i = 0; i < jTableProdutos.getRowCount(); i++) {

                    for (int j = 0; j < jTableProdutos.getColumnCount(); j++) {
                        bw.append(jTableProdutos.getModel().getValueAt(i, j) + ";");
                    }
                    bw.newLine();
                }
                bw.close();
                fw.close();
            }
            
            System.out.println("Dados exportados com sucesso!");
        } catch (HeadlessException | IOException ex) {
            System.out.println("Erro ao exportar dados da jTable!");
        }
    }
