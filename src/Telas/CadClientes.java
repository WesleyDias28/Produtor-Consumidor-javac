    public void cadastroCliente(String nomeArquivo)
    {

	    try
	    {
	        FileWriter arquivo = new FileWriter(nomeArquivo, true);
		BufferedWriter bw = new BufferedWriter(arquivo);
               
                bw.append(jTextFieldNome.getText()+";"+jTextFieldEndereco.getText()+";"+jTextFieldTelefone.getText()+";"+jTextFieldEmail.getText()+";");
                bw.newLine();

                bw.close();
                arquivo.close();

	        System.out.println("Arquivo gerado com sucesso!");
	    }

	    catch(IOException erro)
	    {
	    	System.out.println("Não foi possível escrever o arquivo no disco!");
	    }
	
        }
